package com.company.room.furniture;

/**
 * @author Pavlov Vitaliy
 */

public class Furniture {

     String name;
     String color;
     double width;
     double length;
     double height;

    public Furniture(String name, String color, double width, double length, double height) {
        this.name = name;
        this.color = color;
        this.width = width;
        this.length = length;
        this.height = height;
    }
}
